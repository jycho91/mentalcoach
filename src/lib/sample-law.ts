/**
 * ID로 샘플 법령 찾기
 */
export function getSampleLawById(id: string): SampleLaw | undefined {
  return SAMPLE_LAWS.find(law => law.id === id);
}

/**
 * 카테고리별 샘플 법령 필터링
 */
export function getSampleLawsByCategory(category: string): SampleLaw[] {
  return SAMPLE_LAWS.filter(law => law.category === category);
}
